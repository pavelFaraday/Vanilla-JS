// .push() Array Method allows you to add one or more lements at the and of an array. And it also returns new length of an array

const nums = [1,2,3];
const newNums = nums.push(4, 5, 78);
console.log(nums);
// [ 1, 2, 3, 4, 5, 78 ]
console.log(newNums);
// 6
